
function add (a, b) {
    var sum = a + b
    return sum
}
function subtract (a, b) {
    var remainder = a - b
    return remainder
}
function multiply (a, b) {
    var product = a * b
    return product
}
function divide (a, b) {
    var quotient = a / b
    return quotient
}
function increment (a) {
    a++
    return a
}
function decrement (a) {
    a--
    return a
}
function makeInt(n) {
    return parseInt(n, 10)
  }
function preserveDecimal(string) {
    return parseFloat(string)
}
